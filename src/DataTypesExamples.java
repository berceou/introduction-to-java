public class DataTypesExamples {

    public static void main(String[] args) {
        byte byteExp = 100;
        System.out.println("Byte: " + byteExp);

        short shortExp = 200;
        System.out.println("Short: " + shortExp);

        int intExp = 300;
        System.out.println("Int: " + intExp);

        long longExp = 400;
        System.out.println("Long: " + longExp);

        float floatExp = 500;
        System.out.println("Float: " + floatExp);

        float floatExp2 = 60.04f;
        System.out.println("Float: " + floatExp2);

        double doubleExp = 600.987;
        System.out.println("Double: " + doubleExp);

        double double2Exp = 600;
        System.out.println("Double: " + double2Exp);

        char charExp = 117; // ascii denk gelen karakteri verir
        System.out.println("Char: " + charExp);

        char char2Exp = 'u'; // u
        System.out.println("Char: " + char2Exp);

        boolean boolExp = true;
        System.out.println("Bool: " + boolExp);

        String stringExp = "Hello World";
        System.out.println("String: " + stringExp);


    }
}
