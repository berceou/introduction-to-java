public class SystemOut {
    public static void main(String[] args) {
        System.out.print("BERÇE ÖZÜM");
        System.out.print("UYGUN");
        // output : BERÇE ÖZÜMUYGUN

        System.out.println("");
        System.out.println("Berçe");
        System.out.println("Uygun");
        /* output:
        Berçe
        Uygun
        */

        System.out.println("uzun metinler için"
        + " " + "bu şekilde plus ile printleyebiliriz"
        + " " + "bu stringleri birleştirmek için kullanılır");

        System.out.println(10); // 10 number
        System.out.println(10 + "20"); //1020
        System.out.println(10 + 20); //30
        System.out.print("Merhaba\n");
        // \n means next line
        System.out.print("\nDünya");
        System.out.print("Merhaba\n Dünya");
        // if use \n = println
        System.out.println("Println Dünya");

        //if use \t means 1 tab
        System.out.println("\t Merhaba tablı örnek");



    }
}
